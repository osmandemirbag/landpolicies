def _r(v, digits=1):
    """Round numeric value `v` to `digits` decimal places; return None if `v` is None."""
    return round(v, digits) if v is not None else None


def build_prod_rows():
    all_years = sorted(set(
        list(wheat_prod_raw) + list(rye_prod_raw) + list(barley_prod_raw) +
        list(oats_prod_raw) + list(corn_prod_raw) + list(potatoes_prod_raw) +
        list(sugar_prod_raw) + list(eggs_prod_raw) + list(milk_prod_raw) +
        list(cheese_prod_raw) + list(butter_prod_raw) + list(beef_prod_raw) +
        list(pork_prod_raw) + list(mutton_prod_raw) + list(poultry_prod_raw) +
        list(rice_prod_raw) + list(tomatoes_prod_raw) + list(oranges_prod_raw)
    ))
    rows = []
    for y in all_years:
        w = wheat_prod_raw.get(y)
        ry = rye_prod_raw.get(y)
        ba = barley_prod_raw.get(y)
        oa = oats_prod_raw.get(y)
        co = corn_prod_raw.get(y)
        po = potatoes_prod_raw.get(y)
        su = sugar_prod_raw.get(y)
        eg = eggs_prod_raw.get(y)
        mi = milk_prod_raw.get(y)
        ch = cheese_prod_raw.get(y)
        bu = butter_prod_raw.get(y)
        be = beef_prod_raw.get(y)
        pk = pork_prod_raw.get(y)
        mu = mutton_prod_raw.get(y)
        pl = poultry_prod_raw.get(y)
        ri = rice_prod_raw.get(y)
        to = tomatoes_prod_raw.get(y)
        or_ = oranges_prod_raw.get(y)

        # Converted values (thousand MT)
        wc   = _r(w   * 27.215) if w   is not None else None
        ryc  = _r(ry  * 25.401) if ry  is not None else None
        bac  = _r(ba  * 21.772) if ba  is not None else None
        oac  = _r(oa  * 14.515) if oa  is not None else None
        coc  = _r(co  * 25.401) if co  is not None else None
        poc  = _r(po  * 45.359) if po  is not None else None
        suc  = _r(su  *  0.9072) if su  is not None else None
        egc  = _r(eg  *  0.68)  if eg  is not None else None
        mic  = _r(mi  *  0.4536) if mi  is not None else None
        chc  = _r(ch  *  0.4536) if ch  is not None else None
        buc  = _r(bu  *  0.4536) if bu  is not None else None
        bec  = _r(be  *  0.4536) if be  is not None else None
        pkc  = _r(pk  *  0.4536) if pk  is not None else None
        muc  = _r(mu  *  0.4536) if mu  is not None else None
        plc  = _r(pl  *  0.4536) if pl  is not None else None
        ric  = _r(ri  *  0.4536) if ri  is not None else None
        toc  = _r(to  *  0.9072) if to  is not None else None
        orc  = _r(or_ * 0.034020) if or_ is not None else None

        # Grains aggregate (wheat+rye+barley+oats+corn)
        grains_vals = [wc, ryc, bac, oac, coc]
        grains = _r(sum(v for v in grains_vals if v is not None)) if any(v is not None for v in grains_vals) else None

        # Meat aggregate (beef+pork+mutton+poultry)
        meat_vals = [bec, pkc, muc, plc]
        meat = _r(sum(v for v in meat_vals if v is not None)) if any(v is not None for v in meat_vals) else None

        # Source attribution
        census_years = {1839, 1849, 1859}
        if y in census_years:
            src = "US Agricultural Census; HSUS Series K1-75"
        else:
            src = "USDA/NASS; HSUS Series K1-75, K507-522; USDA Agricultural Statistics"

        rows.append([
            y, wc, ryc, bac, oac, coc, poc, suc, egc, mic, chc, buc,
            bec, pkc, muc, plc, None, toc, orc, grains, meat, ric, src
        ])
    return rows


