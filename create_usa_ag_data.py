def write_sheet(ws, title_text, unit_text, source_text, data_rows):
    """Write a sheet following the Brazil file format exactly."""
    bold = Font(bold=True)
    wrap = Alignment(wrap_text=True, vertical="top")

    # Row 1: title
    ws.cell(row=1, column=1, value=title_text).font = bold
    # Row 2: unit
    ws.cell(row=2, column=1, value=unit_text)
    # Row 3: source
    ws.cell(row=3, column=1, value=source_text)
    # Row 4: column headers
    for col_idx, header in enumerate(HEADERS, start=1):
        cell = ws.cell(row=4, column=col_idx, value=header)
        cell.font = bold
    # Data rows starting row 5
    for row_idx, row_data in enumerate(data_rows, start=5):
        for col_idx, value in enumerate(row_data, start=1):
            ws.cell(row=row_idx, column=col_idx, value=value)

    # Column widths
    ws.column_dimensions["A"].width = 8
    for col_letter in "BCDEFGHIJKLMNOPQRSTUV":
        ws.column_dimensions[col_letter].width = 11
    ws.column_dimensions["W"].width = 55  # Source column
    ws.row_dimensions[2].height = 30
    ws.row_dimensions[3].height = 30
    ws.cell(row=2, column=1).alignment = wrap
    ws.cell(row=3, column=1).alignment = wrap


